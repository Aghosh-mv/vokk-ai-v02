def compile_runtime_source(source: str) -> Dict[str, Any]:
    parsed = parse_runtime_source(source)
    return {
        "kind": "runtime",
        "name": parsed["apps"][0]["name"] if parsed["apps"] else "VokkRuntime",
        "parsed": parsed,
        "python": emit_runtime_python(parsed),
        "go": emit_runtime_go(parsed),
        "java": emit_runtime_java(parsed),
        "vokkscript": emit_runtime_vokkscript(parsed),
        "client_js": emit_runtime_client_js(parsed),
        "html": render_runtime_preview(parsed, source),
    }

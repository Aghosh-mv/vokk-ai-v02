#!/usr/bin/env python3
"""
vokk_runtime_lang.py — early VOKK backend/runtime language layer.

This is not a full runtime replacement. It lets VOKK describe backend/server
surfaces in its own language and compile them into inspectable plans and host
stubs.

Supported top-level blocks:
  app NAME { ... }
  route NAME { ... }
  store NAME { ... }
  session NAME { ... }
  action NAME { ... }
  component NAME { ... }
"""

from __future__ import annotations

import html
import json
import re
from typing import Any, Dict, List


RUNTIME_KINDS = ("app", "route", "store", "session", "action", "component")


def _tokens(line: str) -> List[str]:
    return re.findall(r'"[^"]*"|\S+', line.strip())


def _clean(v: str) -> str:
    return v.strip().strip('"').strip("'")


def _extract_runtime_blocks(source: str) -> List[Dict[str, str]]:
    blocks: List[Dict[str, str]] = []
    n = len(source)
    pattern = r"\b(" + "|".join(RUNTIME_KINDS) + r")\s+([A-Za-z_]\w*)\s*\{"
    for m in re.finditer(pattern, source, re.M):
        kind, name = m.group(1), m.group(2)
        depth, j = 1, m.end()
        while j < n and depth:
            depth += (source[j] == "{") - (source[j] == "}")
            j += 1
        blocks.append({"kind": kind, "name": name, "body": source[m.end():j - 1]})
    return blocks


def _parse_body(body: str) -> Dict[str, Any]:
    spec: Dict[str, Any] = {"raw_lines": []}
    for raw in body.splitlines():
        line = raw.strip()
        if not line or line.startswith("#"):
            continue
        spec["raw_lines"].append(line)
        t = _tokens(line)
        key = t[0].lower()
        vals = [_clean(v) for v in t[1:]]
        if not vals:
            spec[key] = True
        elif len(vals) == 1:
            spec[key] = vals[0]
        else:
            spec[key] = vals
    return spec


def parse_runtime_source(source: str) -> Dict[str, Any]:
    blocks = _extract_runtime_blocks(source)
    apps: List[Dict[str, Any]] = []
    routes: List[Dict[str, Any]] = []
    stores: List[Dict[str, Any]] = []
    sessions: List[Dict[str, Any]] = []
    actions: List[Dict[str, Any]] = []
    components: List[Dict[str, Any]] = []
    for b in blocks:
        row = {"kind": b["kind"], "name": b["name"], "spec": _parse_body(b["body"])}
        if b["kind"] == "app":
            apps.append(row)
        elif b["kind"] == "route":
            routes.append(row)
        elif b["kind"] == "store":
            stores.append(row)
        elif b["kind"] == "session":
            sessions.append(row)
        elif b["kind"] == "action":
            actions.append(row)
        elif b["kind"] == "component":
            components.append(row)
    return {
        "apps": apps,
        "routes": routes,
        "stores": stores,
        "sessions": sessions,
        "actions": actions,
        "components": components,
        "counts": {
            "apps": len(apps),
            "routes": len(routes),
            "stores": len(stores),
            "sessions": len(sessions),
            "actions": len(actions),
            "components": len(components),
        },
    }


def _py_list(v: Any) -> str:
    if isinstance(v, list):
        return "[" + ", ".join(repr(x) for x in v) + "]"
    if v is None:
        return "None"
    return repr(v)


def emit_runtime_python(parsed: Dict[str, Any]) -> str:
    lines = [
        "# Generated by VOKK runtime compiler preview",
        "APP = {",
    ]
    app = parsed["apps"][0] if parsed["apps"] else {"name": "VokkApp", "spec": {}}
    lines.extend([
        f"    'name': {app['name']!r},",
        f"    'spec': {json.dumps(app['spec'], indent=4)},",
        "}",
        "",
        "ROUTES = [",
    ])
    for route in parsed["routes"]:
        spec = route["spec"]
        lines.append("    {")
        lines.append(f"        'name': {route['name']!r},")
        lines.append(f"        'method': {_py_list(spec.get('method', 'GET'))},")
        lines.append(f"        'path': {_py_list(spec.get('path', '/'))},")
        lines.append(f"        'auth': {_py_list(spec.get('auth', 'optional'))},")
        lines.append(f"        'action': {_py_list(spec.get('action'))},")
        lines.append("    },")
    lines.extend([
        "]",
        "",
        "STORES = " + json.dumps([{"name": s["name"], **s["spec"]} for s in parsed["stores"]], indent=4),
        "SESSIONS = " + json.dumps([{"name": s["name"], **s["spec"]} for s in parsed["sessions"]], indent=4),
        "ACTIONS = " + json.dumps([{"name": s["name"], **s["spec"]} for s in parsed["actions"]], indent=4),
        "COMPONENTS = " + json.dumps([{"name": s["name"], **s["spec"]} for s in parsed["components"]], indent=4),
    ])
    return "\n".join(lines)


def emit_runtime_client_js(parsed: Dict[str, Any]) -> str:
    routes = []
    for route in parsed["routes"]:
        spec = route["spec"]
        routes.append({
            "name": route["name"],
            "method": spec.get("method", "GET"),
            "path": spec.get("path", "/"),
            "auth": spec.get("auth", "optional"),
            "action": spec.get("action"),
        })
    return (
        "// Generated by VOKK runtime compiler preview\n"
        f"export const vokkRuntimeRoutes = {json.dumps(routes, indent=2)};\n"
    )


def emit_runtime_go(parsed: Dict[str, Any]) -> str:
    lines = [
        "// Generated by VOKK runtime compiler preview",
        "package main",
        "",
        'import "net/http"',
        "",
    ]
    for route in parsed["routes"]:
        spec = route["spec"]
        path = spec.get("path", "/")
        method = str(spec.get("method", "GET")).upper()
        fname = route["name"][0].upper() + route["name"][1:] + "Handler"
        lines.extend([
            f"func {fname}(w http.ResponseWriter, r *http.Request) {{",
            f'    if r.Method != "{method}" {{',
            '        w.WriteHeader(http.StatusMethodNotAllowed)',
            "        return",
            "    }",
            f'    w.Write([]byte("VOKK route: {route["name"]}"))',
            "}",
            "",
        ])
        lines.append(f'// route {method} {path}')
    lines.extend([
        "func main() {",
    ])
    for route in parsed["routes"]:
        spec = route["spec"]
        path = spec.get("path", "/")
        fname = route["name"][0].upper() + route["name"][1:] + "Handler"
        lines.append(f'    http.HandleFunc("{path}", {fname})')
    lines.extend([
        '    http.ListenAndServe(":8080", nil)',
        "}",
    ])
    return "\n".join(lines)


def emit_runtime_java(parsed: Dict[str, Any]) -> str:
    app = parsed["apps"][0]["name"] if parsed["apps"] else "VokkApp"
    lines = [
        "// Generated by VOKK runtime compiler preview",
        "import java.util.List;",
        "",
        f"public class {app}Runtime " + "{",
        "    record RouteSpec(String name, String method, String path, String auth, String action) {}",
        "    static final List<RouteSpec> ROUTES = List.of(",
    ]
    for i, route in enumerate(parsed["routes"]):
        spec = route["spec"]
        comma = "," if i < len(parsed["routes"]) - 1 else ""
        lines.append(
            f'        new RouteSpec("{route["name"]}", "{spec.get("method", "GET")}", "{spec.get("path", "/")}", "{spec.get("auth", "optional")}", "{spec.get("action", "")}"){comma}'
        )
    lines.extend([
        "    );",
        "",
        "    public static void main(String[] args) {",
        '        System.out.println("VOKK runtime preview");',
        "        for (RouteSpec route : ROUTES) {",
        '            System.out.println(route.method() + " " + route.path() + " -> " + route.action());',
        "        }",
        "    }",
        "}",
    ])
    return "\n".join(lines)


def emit_runtime_vokkscript(parsed: Dict[str, Any]) -> str:
    lines: List[str] = []
    for kind in ("apps", "stores", "sessions", "actions", "routes", "components"):
        for item in parsed[kind]:
            block_kind = item["kind"]
            lines.append(f"{block_kind} {item['name']} {{")
            for raw in item["spec"].get("raw_lines", []):
                lines.append(f"  {raw}")
            lines.append("}")
            lines.append("")
    return "\n".join(lines).rstrip() + "\n"


